class Solution {

    public static void main(String[] args) {
        System.out.println(isPalindrome(101));
    }

	public static boolean isPalindrome(int x) {
		if ( x < 0 || x >= Integer.MAX_VALUE ) {
			return false;
		}
		if ( x == 0 ) {
			return true;
		}

		







		return true;

	}


}
